def rgb888_to_rgb565(r: int, g: int, b: int) -> int:
    return ((r & 0xF8) << 8) | ((g & 0xFC) << 3) | (b >> 3)


class Framebuffer:
    """論文240 RGB565。バンド単位のクリップ描画をサポート。"""

    def __init__(self) -> None:
        self.width = SCREEN_WIDTH
        self.height = SCREEN_HEIGHT
        self.buffer_height = BAND_HEIGHT
        self.pixels = array("H", [0] * (self.width * self.height))
        self.band_index = 0
        self.band_y0 = 0
        self.band_rows = self.buffer_height
        self._band_buf = array("H", [0] * (self.width * self.buffer_height))

    def band_count(self) -> int:
        return (self.height + self.buffer_height - 1) // self.buffer_height

    def begin_band(self, band: int) -> None:
        self.band_index = band
        self.band_y0 = band * self.buffer_height
        remaining = self.height - self.band_y0
        self.band_rows = min(self.buffer_height, max(0, remaining))

    def end_band(self) -> None:
        if self.band_rows <= 0:
            return
        dst = self.band_y0 * self.width
        src = 0
        for row in range(self.band_rows):
            self.pixels[dst : dst + self.width] = self._band_buf[src : src + self.width]
            dst += self.width
            src += self.width

    def band_top(self) -> int:
        return self.band_y0

    def band_bottom(self) -> int:
        return self.band_y0 + self.band_rows

    def rect_in_band(self, y: int, h: int) -> bool:
        return (y + h) > self.band_top() and y < self.band_bottom()

    def clear(self, color: int) -> None:
        if self.band_rows <= 0:
            return
        n = self.width * self.band_rows
        self._band_buf[:n] = array("H", [color & 0xFFFF] * n)

    def _clip_rect(self, x: int, y: int, w: int, h: int) -> tuple[int, int, int, int] | None:
        if w <= 0 or h <= 0:
            return None
        x0 = max(0, x)
        y0 = max(self.band_top(), y)
        x1 = min(self.width, x + w)
        y1 = min(self.band_bottom(), y + h)
        if x0 >= x1 or y0 >= y1:
            return None
        return x0, y0, x1, y1

    def fill_rect(self, x: int, y: int, w: int, h: int, color: int) -> None:
        clipped = self._clip_rect(x, y, w, h)
        if not clipped:
            return
        x0, y0, x1, y1 = clipped
        color &= 0xFFFF
        for row in range(y0, y1):
            off = (row - self.band_y0) * self.width
            self._band_buf[off + x0 : off + x1] = array("H", [color] * (x1 - x0))

    def draw_line(self, x0: int, y0: int, x1: int, y1: int, color: int) -> None:
        color &= 0xFFFF
        dx = abs(x1 - x0)
        dy = -abs(y1 - y0)
        sx = 1 if x0 < x1 else -1
        sy = 1 if y0 < y1 else -1
        err = dx + dy
        x, y = x0, y0
        while True:
            if 0 <= x < self.width and self.band_top() <= y < self.band_bottom():
                self._band_buf[(y - self.band_y0) * self.width + x] = color
            if x == x1 and y == y1:
                break
            e2 = 2 * err
            if e2 >= dy:
                err += dy
                x += sx
            if e2 <= dx:
                err += dx
                y += sy

    def fill_circle(self, cx: int, cy: int, r: int, color: int) -> None:
        color &= 0xFFFF
        for y in range(cy - r, cy + r + 1):
            if not self.rect_in_band(y, 1):
                continue
            dx = int(math.sqrt(max(0, r * r - (y - cy) ** 2)))
            self.fill_rect(cx - dx, y, dx * 2 + 1, 1, color)

    def draw_circle(self, cx: int, cy: int, r: int, color: int) -> None:
        color &= 0xFFFF
        x = r
        y = 0
        err = 0
        while x >= y:
            for px, py in (
                (cx + x, cy + y),
                (cx + y, cy + x),
                (cx - y, cy + x),
                (cx - x, cy + y),
                (cx - x, cy - y),
                (cx - y, cy - x),
                (cx + y, cy - x),
                (cx + x, cy - y),
            ):
                if 0 <= px < self.width and self.band_top() <= py < self.band_bottom():
                    self._band_buf[(py - self.band_y0) * self.width + px] = color
            y += 1
            err += 1 + 2 * y
            if 2 * (err - x) + 1 > 0:
                x -= 1
                err += 1 - 2 * x

    def blit_rgb565(
        self,
        src: array | list[int],
        src_w: int,
        src_h: int,
        dx: int,
        dy: int,
        sx: int = 0,
        sy: int = 0,
        sw: int | None = None,
        sh: int | None = None,
        key_color: int | None = None,
    ) -> None:
        if sw is None:
            sw = src_w - sx
        if sh is None:
            sh = src_h - sy
        for row in range(sh):
            screen_y = dy + row
            if screen_y < self.band_top() or screen_y >= self.band_bottom():
                continue
            src_y = sy + row
            if src_y < 0 or src_y >= src_h:
                continue
            dst_off = (screen_y - self.band_y0) * self.width
            for col in range(sw):
                screen_x = dx + col
                if screen_x < 0 or screen_x >= self.width:
                    continue
                src_x = sx + col
                if src_x < 0 or src_x >= src_w:
                    continue
                pix = src[src_y * src_w + src_x] & 0xFFFF
                if key_color is not None and pix == (key_color & 0xFFFF):
                    continue
                self._band_buf[dst_off + screen_x] = pix

    def draw_tile(
        self,
        dx: int,
        dy: int,
        tile_w: int,
        tile_h: int,
        sheet_cols: int,
        sheet: array,
        sheet_w: int,
        sheet_h: int,
        tile_index: int,
        key_color: int | None = None,
    ) -> None:
        col = tile_index % sheet_cols
        row = tile_index // sheet_cols
        sx = col * tile_w
        sy = row * tile_h
        self.blit_rgb565(
            sheet, sheet_w, sheet_h, dx, dy, sx, sy, tile_w, tile_h, key_color
        )

    def draw_char_8x8(
        self, x: int, y: int, ch: str, fg: int, bg: int, use_bg: bool = True
    ) -> None:
        if len(ch) != 1:
            return
        c = ord(ch)
        if c < 32 or c > 127:
            return
        glyph = FONT_8X8[c - 32]
        fg &= 0xFFFF
        bg &= 0xFFFF
        for row in range(8):
            py = y + row
            if py < self.band_top() or py >= self.band_bottom():
                continue
            bits = glyph[row]
            dst_off = (py - self.band_y0) * self.width
            for col in range(8):
                px = x + col
                if px < 0 or px >= self.width:
                    continue
                if bits & (1 << col):
                    self._band_buf[dst_off + px] = fg
                elif use_bg:
                    self._band_buf[dst_off + px] = bg

    def to_rgb888_bytes(self) -> bytes:
        out = bytearray(self.width * self.height * 3)
        idx = 0
        for pix in self.pixels:
            r, g, b = rgb565_to_rgb888(pix)
            out[idx] = r
            out[idx + 1] = g
            out[idx + 2] = b
            idx += 3
        return bytes(out)

    @staticmethod
    def load_bin_pixels(path: str, width: int, height: int) -> array:
        data = open(path, "rb").read()
        expected = width * height * 2
        if len(data) != expected:
            raise ValueError(f"{path}: size {len(data)} != {expected} ({width}x{height})")
        pixels = array("H")
        pixels.frombytes(data)
        if struct.pack("@H", 1) != struct.pack("<H", 1):
            pixels.byteswap()
        return pixels
